package be.Balor.Manager.Commands.Server;

import org.bukkit.command.CommandSender;

import be.Balor.Manager.Commands.CommandArgs;
import be.Balor.Manager.Commands.CoreCommand;
import be.Balor.Tools.Utils;
import be.Balor.bukkit.AdminCmd.ACHelper;

/**
 * @author Balor (aka Antoine Aflalo)
 * 
 */
public class ReloadAll extends CoreCommand {

	/**
	 * 
	 */
	public ReloadAll() {
		permNode = "admincmd.server.reload";
		cmdName = "bal_reloadall";
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * be.Balor.Manager.ACCommands#execute(org.bukkit.command.CommandSender,
	 * java.lang.String[])
	 */
	@Override
	public void execute(CommandSender sender, CommandArgs args) {
		ACHelper.getInstance().saveElapsedTime();
		boolean bcast = ACHelper.getInstance().getConfBoolean("broadcastServerReload");
		sender.getServer().reload();
		if (bcast)
			Utils.broadcastMessage(Utils.I18n("serverReload"));
		else
			Utils.sI18n(sender, "serverReload");
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see be.Balor.Manager.ACCommands#argsCheck(java.lang.String[])
	 */
	@Override
	public boolean argsCheck(String... args) {
		return true;
	}

}
